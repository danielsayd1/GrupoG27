    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        // TODO add your handling code here:

        double puntoX = Double.parseDouble(txtPx.getText());
        double puntoY = Double.parseDouble(txtPy.getText());

        double esiX = Double.parseDouble(txtP1X.getText());
        double esiY = Double.parseDouble(txtP1Y.getText());

        double eidX = Double.parseDouble(txtP2X.getText());
        double eidY = Double.parseDouble(txtP2Y.getText());

        if ((esiX <= puntoX && puntoX <= eidX) && (esiY >= puntoY && puntoY >= eidY)) {
            JOptionPane.showMessageDialog(rootPane, "SI", "Respuesta", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(rootPane, "NO", "Respuesta", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_btnAceptarActionPerformed

    private boolean isLlenoP1() {
        return (!txtP1X.getText().isEmpty() && !txtP1Y.getText().isEmpty());
    }

    private boolean isLlenoP2() {
        return (!txtP2X.getText().isEmpty() && !txtP2Y.getText().isEmpty());
    }

